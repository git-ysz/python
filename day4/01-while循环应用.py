"""
1、准备做加法运算的数据1-100，增量为1
2、将变量保存将来运算的结果
3、循环做加法运算
4、打印结果
5、验证结果正确性
"""
# 需求1：1-100数字累加和 -- 1 + 2 + 3 + ... + 100 = 结果
# 准备数据，保存结果的变量
i = 1
res1 = 0
# 循环运算
while i <= 100:
    res1 += i
    i += 1
print(res1)  # 5050

# 需求2：1-100内偶数累加和 -- 2 + 4 + 6 + ... + 98 + 100 = 结果
# 循环运算

# 方法一(bad) -- 循环100次
i = 1
res2 = 0
while i <= 100:
    if i % 2 == 0:
        res2 += i
    i += 1
print(res2)  # 2550

# 方法二(good) -- 循环50次
i = 2
res3 = 0
while i <= 100:
    res3 += i
    i += 2
print(res3)  # 2550
